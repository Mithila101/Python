# Declare a string
name = "Mithila"

# Print the string
print("Name:", name)

# String concatenation
greeting = "Hello, " + name + "!"
print(greeting)

# String length
print("Length of name:", len(name))

# Accessing characters
print("First letter:", name[0])
print("Last letter:", name[-1])

# String slicing
print("First 3 letters:", name[:3])

# Changing case
print("Uppercase:", name.upper())
print("Lowercase:", name.lower())

# Checking if substring exists
print("Does 'thil' exist in name?", "thil" in name)
